export class students{
    name = '';
    dateOfBirth = '';
    code = '';
    status = '';
    email = '';

    constructor(name, dateOfBirth, code, status, email){
        this.name = name;
        this.dateOfBirth = dateOfBirth;
        this.code = code;
        this.status = status;
        this.email = email;
    }
}